PUBLISHED = "2026-06-29"


def _article_jsonld(title, desc, url, faqs):
    base = BASE_URL.rstrip("/")
    article = {
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": title,
        "description": desc,
        "inLanguage": "ko-KR",
        "datePublished": PUBLISHED,
        "dateModified": PUBLISHED,
        "image": f"{base}/assets/cover-main.png",
        "author": {"@type": "Organization", "name": R.AUTHOR},
        "publisher": {"@type": "Organization", "name": BRAND,
                      "logo": {"@type": "ImageObject", "url": f"{base}/assets/icon-512.png"}},
        "mainEntityOfPage": {"@type": "WebPage", "@id": url + "#webpage"},
    }
    out = ('<script type="application/ld+json">\n'
           + json.dumps(article, ensure_ascii=False) + "\n</script>\n")
    if faqs:
        out += R.faq_jsonld(faqs)
    return out


def _page(slug, title, desc, h1, sections, faqs):
    url = BASE_URL.rstrip("/") + f"/column/{slug}/"
    body = "\n".join(sections)
    body += R.checklist_block() + R.faq_block(faqs) + R.who_how_why() + R.byline_block()
    body += (f'<section class="cta"><h2>예약문의</h2>'
             f'<p>방문 위치(도로명 주소)와 희망 시간을 알려주시면 가능 여부를 바로 확인해 드립니다.</p>'
             f'<a class="cta-phone" href="tel:{PHONE}">{PHONE_DISPLAY}</a></section>')
    return {
        "path": f"column/{slug}/",
        "title": f"{title} | 간다GO 서울 출장마사지",
        "desc": desc,
        "h1": h1,
        "body": body,
        "extra_head": _article_jsonld(title, desc, url, faqs),
        "breadcrumb": [("가이드", "/column/"), (h1, "")],
        "image": "/assets/cover-main.png",
        "image_alt": f"{h1} 안내 이미지",
    }


