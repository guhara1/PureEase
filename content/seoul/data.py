# ── 한글 동명 → URL 슬러그 자동 변환 (ROMAJA 미등록 동 대비) ──
_LEAD = ['g','kk','n','d','tt','r','m','b','pp','s','ss','','j','jj','ch','k','t','p','h']
_VOWEL = ['a','ae','ya','yae','eo','e','yeo','ye','o','wa','wae','oe','yo','u','wo','we','wi','yu','eu','ui','i']
_TAIL = ['','k','k','k','n','n','n','t','l','k','m','l','l','l','p','l','m','p','p','t','t','ng','t','t','k','t','p','t']

def romanize(s):
    out = []
    for ch in s:
        o = ord(ch)
        if 0xAC00 <= o <= 0xD7A3:
            c = o - 0xAC00
            out.append(_LEAD[c // 588] + _VOWEL[(c % 588) // 28] + _TAIL[c % 28])
        elif ch.isalnum():
            out.append(ch.lower())
    return "".join(out)

def slug_for(name):
    """대표 행정동 슬러그. 수기 매핑(ROMAJA) 우선, 없으면 자동 로마자화."""
    return ROMAJA.get(name) or romanize(name)

